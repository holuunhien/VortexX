# viet chuong trinh nhap 2 so nguyen duong a va b . tinh tong , hieu , thuong , tich cua 2 so do
a = int(input("nhap so nguyen duong a: "))
b = int(input("nhap so nguyen duong b: "))
tong = a + b
hieu = a - b
tich = a * b
thuong = a / b
print("tong cua 2 so la:", tong)
print("hieu cua 2 so la:", hieu)
print("tich cua 2 so la:", tich)
print("thuong cua 2 so la:", thuong)